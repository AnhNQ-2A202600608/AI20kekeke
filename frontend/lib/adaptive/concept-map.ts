export function isUsableAdaptiveConceptId(conceptId: string | undefined): conceptId is string {
  return Boolean(conceptId && !LEGACY_STATIC_CONCEPT_IDS.has(conceptId));
}

export function resolveAdaptiveConceptId(
  setId: string,
  conceptMasteries: Record<string, { conceptId?: string }>,
) {
  const canonicalConceptId = CANONICAL_ADAPTIVE_CONCEPT_IDS_BY_SET_ID[setId]?.[0];
  if (canonicalConceptId) {
    return canonicalConceptId;
  }

  const masteredConceptId = conceptMasteries[setId]?.conceptId;
  if (isUsableAdaptiveConceptId(masteredConceptId)) {
    return masteredConceptId;
  }

  return null;
}

